#include "3-calc.h"

/**
 * op_add - add two ints
 * @a: int one
 * @b: int second
 * return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two ints
 * @a: first int
 * @b: second int to be subtracted
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the moltiple of two int
 * @a: int 1
 * @b: int 2
 * Return: the result of multiplicion
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - results in the division
 * @a: first number
 * @b: second number
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the moduli of two int
 * @a: int first
 * @b: int second
 * Return: moduli recult
 */
int op_mod(int a, int b)
{
	return (a % b);
}
